import type React from "react";
import { forwardRef } from "react";

interface IconProps extends Omit<React.SVGProps<SVGSVGElement>, "viewBox"> {
	readonly size?: number | string;
	readonly "aria-label"?: string;
	readonly title?: string;
	readonly description?: string;
}

function createIcon(
	displayName: string,
	viewBox: string,
	pathData: string | React.ReactElement,
): React.ForwardRefExoticComponent<
	IconProps & React.RefAttributes<SVGSVGElement>
> {
	const IconComponent = forwardRef<SVGSVGElement, IconProps>(
		(
			{
				size = 24,
				className,
				"aria-label": ariaLabel,
				title,
				description,
				...props
			},
			ref,
		) => {
			const hasSemanticMeaning = ariaLabel || title;

			return (
				<svg
					ref={ref}
					xmlns="http://www.w3.org/2000/svg"
					viewBox={viewBox}
					fill="currentColor"
					width={size}
					height={size}
					className={className}
					aria-hidden={hasSemanticMeaning ? undefined : true}
					aria-label={ariaLabel}
					role={hasSemanticMeaning ? "img" : undefined}
					{...props}
				>
					{title && <title>{title}</title>}
					{description && <desc>{description}</desc>}
					{typeof pathData === "string" ? <path d={pathData} /> : pathData}
				</svg>
			);
		},
	);

	IconComponent.displayName = displayName;
	return IconComponent;
}

